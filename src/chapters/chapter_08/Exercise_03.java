package chapters.chapter_08;

public class Exercise_03 {
    public static void main(String[] args) {

        char[][] answers = {
                {'A', 'B', 'A', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'D', 'B', 'A', 'B', 'C', 'A', 'E', 'E', 'A', 'D'},
                {'E', 'D', 'D', 'A', 'C', 'B', 'E', 'E', 'A', 'D'},
                {'C', 'B', 'A', 'E', 'D', 'C', 'E', 'E', 'A', 'D'},
                {'A', 'B', 'D', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'B', 'B', 'E', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'B', 'B', 'A', 'C', 'C', 'D', 'E', 'E', 'A', 'D'},
                {'E', 'B', 'E', 'C', 'C', 'D', 'E', 'E', 'A', 'D'}};

        char[] keys = {'D', 'B', 'D', 'C', 'C', 'D', 'A', 'E', 'A', 'D'};

        int[] results = new int[answers.length];

        for (int i = 0; i < answers.length; i++) {
            int correctCount = 0;
            for (int j = 0; j < answers[i].length; j++) {
                if (answers[i][j] == keys[j])
                    correctCount++;
            }
            results[i] = correctCount;
        }
        displayStudents(results);
    }

    public static void displayStudents(int[] results) {
        for (int i = 0; i < results.length; i++) {
            int min = Integer.MAX_VALUE;

            int minIndex = -1;

            for (int j = 0; j < results.length; j++) {
                if (results[j] < min) {
                    min = results[j];
                    minIndex = j;
                }
            }
            System.out.println("Student " + minIndex + "'s correct count is " + min);
            results[minIndex] = 11;
        }
    }
}


