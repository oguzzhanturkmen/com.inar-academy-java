public class Checkpoint_07 {
	public static void main(String[] args) {
		
		int number = 30;
		
		if (number % 2 == 0)
			System.out.println(number + " is even.");
			System.out.println(number + " is odd.");
			
			
		if (number % 2 == 0)
			System.out.println(number + " is even.");
		else
			System.out.println(number + " is odd.");		
		
		number = 35;
		
		if (number % 2 == 0)
			System.out.println(number + " is even.");
			System.out.println(number + " is odd.");
			
			
		if (number % 2 == 0)
			System.out.println(number + " is even.");
		else
			System.out.println(number + " is odd.");		
		
		
				
	}

}
